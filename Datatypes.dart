void main() {
  // Demonstrating int data type
  int age = 25;
  print('Age: $age');

  //Demonstrating double data type
  double weight = 65.5;
  print('Weight: $weight');

  //Demonstrating String data type
  String name = 'Esther Murunga';
  print('Name: $name');

  //Demonstrating List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  //Demonstrating Map data type
  Map<String, int> studentScores = {'Math': 90, 'Science': 85, 'English': 88};
  print('Student Scores; $studentScores');
}
